def home(request):
    # Tweets
    twitter = Twitter(auth=OAuth(settings.TOKEN,
                                 settings.TOKEN_KEY,
                                 settings.SECRET,
                                 settings.SECRET_KEY))

    tweets = twitter.statuses.user_timeline(screen_name='NaranbayarU', count=5)

    utcnow = datetime.utcnow()
    datetime_format = '%Y-%m-%d %H:%M:%S UTC'
    hackerrank_url = 'http://www.hackerrank.com/calendar/feed.rss'

    # Contests
    active_contests, upcoming_contests = [], []
    feeds = feedparser.parse(hackerrank_url)
    for feed in reversed(feeds['entries']):
        start_time = datetime.strptime(feed['starttime'], datetime_format)
        end_time = datetime.strptime(feed['endtime'], datetime_format)

        current_contest = {        
            'url': feed['url'],
            'title': feed['title'],
            'start_time': (start_time + timedelta(hours=8)).strftime('%Y-%m-%d %H:%M:%S'),
            'end_time': (end_time + timedelta(hours=8)).strftime('%Y-%m-%d %H:%M:%S'),
            'start_time_left': timedelta_to_string(start_time - utcnow),
            'end_time_left': timedelta_to_string(end_time - utcnow)
        }
        
        if start_time <= utcnow <= end_time:
            active_contests.append(current_contest) # Active contests
        else:
            upcoming_contests.append(current_contest)  # Upcoming contests

    return render(request, 'index.html', {
        'current_time': (utcnow + timedelta(hours=8)).strftime('%Y-%m-%d %H:%M:%S'),
        'active_contests': active_contests,
        'upcoming_contests': upcoming_contests[:10],
        'tweets': tweets,
        'tab' : 'home'
    })

def timedelta_to_string(td_object):
    seconds = int(td_object.total_seconds())
    periods = [
            ('жил',        60*60*24*365),
            ('сар',        60*60*24*30),
            ('өдөр',       60*60*24),
            ('цаг',        60*60),
            ('минут',      60)
            ]

    strings=[]
    for period_name,period_seconds in periods:
        if seconds > period_seconds:
            period_value , seconds = divmod(seconds,period_seconds)     
            strings.append("%s %s" % (period_value, period_name))

    return ", ".join(strings)

